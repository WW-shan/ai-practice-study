# -*- coding: utf-8 -*-
import json
from pathlib import Path

lessons = []

def t(s):
    return " ".join(str(s).split())

def ev(ref, quote):
    return {"ref": ref, "quote": t(quote)}

def sec(heading, body, bullets):
    return {"heading": heading, "body": [t(x) for x in body], "bullets": [t(x) for x in bullets]}

def ex(tp, question, answer):
    return {"type": tp, "question": t(question), "answer": t(answer)}

def ans(title, text):
    return {"title": title, "text": t(text)}

def quiz(question, options, correct, explain):
    return {"question": t(question), "options": [t(x) for x in options], "correct": correct, "explain": t(explain)}

def add(id, chapter, order, title, subtitle, evidence, intent, learning, exam, traps, answers, quizzes):
    lessons.append({
        "id": id, "status": "ready", "chapter": chapter, "order": f"{order:02d}",
        "title": title, "subtitle": subtitle, "sourceEvidence": evidence,
        "intent": [t(x) for x in intent], "learning": learning, "exam": exam,
        "traps": [t(x) for x in traps], "answers": answers, "quiz": quizzes,
        "review": {
            "sourceChecked": True,
            "teachesFromScratch": True,
            "examAligned": True,
            "selfReviewed": True,
            "reviewNote": "已按复习要点和课件提取资料核对：包含课件证据、学习解释、考法、易错点、标准答案和自测。"
        }
    })

